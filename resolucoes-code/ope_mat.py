# Solicita uma string do usuário
entrada_string = input("Digite uma string: ")

# Solicita um número inteiro do usuário
entrada_numero = int(input("Digite um número inteiro: "))

# Repete a string o número de vezes especificado
string_repetida = entrada_string * entrada_numero

# Exibe a string repetida
print("A string repetida é:", string_repetida)
